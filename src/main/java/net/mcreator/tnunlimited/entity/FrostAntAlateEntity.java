	@Override
	protected void registerGoals() {
		super.registerGoals();

		this.targetSelector.addGoal(1, new NearestAttackableTargetGoal(this, ServerPlayer.class, false, false));
		this.targetSelector.addGoal(2, new NearestAttackableTargetGoal(this, Player.class, false, false));
		this.targetSelector.addGoal(3, new NearestAttackableTargetGoal(this, Zombie.class, false, false));
		this.targetSelector.addGoal(4, new HurtByTargetGoal(this).setAlertOthers());
		this.goalSelector.addGoal(5, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (FrostAntAlateEntity.this.getTarget() != null && !FrostAntAlateEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return FrostAntAlateEntity.this.getMoveControl().hasWanted() && FrostAntAlateEntity.this.getTarget() != null && FrostAntAlateEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = FrostAntAlateEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				FrostAntAlateEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = FrostAntAlateEntity.this.getTarget();
				if (FrostAntAlateEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					FrostAntAlateEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = FrostAntAlateEntity.this.distanceToSqr(livingentity);
					if (d0 < 16) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						FrostAntAlateEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
					}
				}
			}
		});
		this.goalSelector.addGoal(6, new RandomStrollGoal(this, 1.2, 20) {

			@Override
			protected Vec3 getPosition() {
				RandomSource random = FrostAntAlateEntity.this.getRandom();
				double dir_x = FrostAntAlateEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = FrostAntAlateEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = FrostAntAlateEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}

		});
		this.goalSelector.addGoal(7, new RandomStrollGoal(this, 1));
		this.goalSelector.addGoal(8, new RandomLookAroundGoal(this));
		this.goalSelector.addGoal(9, new FloatGoal(this));

	}

